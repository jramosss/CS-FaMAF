module Hal.Symbols where
{- Módulo para definir los símbolos de la sintaxis del lenguaje.
Si se quiere modificar la sintaxis, solo hay que tocar este módulo y no
el parser ni el pretty print.
-}

-- Operadores Booleanos
andSym = "&&"
orSym = "||"
notSym = "not"

-- Operadores Enteros
plusSym = "+"
timesSym = "*"
substrSym = "-"
divSym = "/"
modSym = "%"

-- Relaciones
eqSym = "="
ltSym = "<"








